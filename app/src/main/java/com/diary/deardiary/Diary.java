public class Diary {
    private String Diaryname;
    private String DayDiary;
    private Integer DateDiary;
    private String DataDiary;
    private String MonthDiary;


    public Integer getDateDiary() {
        return DateDiary;
    }

    public void setDateDiary(Integer dateDiary) {
        DateDiary = dateDiary;
    }

    public String getDayDiary() {
        return DayDiary;
    }

    public void setDayDiary(String dayDiary) {
        DayDiary = dayDiary;
    }

    public String getMonthDiary() {
        return MonthDiary;
    }

    public void setMonthDiary(String monthDiary) {
        MonthDiary = monthDiary;
    }

    public String getDataDiary() {
        return DataDiary;
    }

    public void setDataDiary(String dataDiary) {
        DataDiary = dataDiary;
    }

    public String getDiaryname() {
        return Diaryname;
    }

    public void setDiaryname(String diaryname) {
        Diaryname = diaryname;
    }

}
